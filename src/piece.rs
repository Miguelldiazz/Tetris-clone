#[derive(Copy, Clone)]
pub struct Piece {
    form: usize,
    rotation: usize,
    color: usize,
    position: (i8, i8),
}

impl Piece {
    pub fn new() -> Piece {
        let mut rng = rand::thread_rng();
        let f = rng.gen_range(0, 7);
        let r = rng.gen_range(0, 4);
        let c = rng.gen_range(0, 5);

        Piece {
            form: f,
            rotation: r,
            position: STARTING_POSITIONS[f as usize][r as usize],
            color: c,
        }
    }

    pub fn get_position(&self) -> (i8, i8) {
        self.position
    }

    pub fn get_block_at(&self, x: usize, y: usize) -> i8 {
        PIECES[self.form][self.rotation][y][x]
    }

    pub fn rotate(&mut self) {
        self.rotation += 1;
        if self.rotation == 4 {
            self.rotation = 0;
        }
    }

    pub fn rotate_back(&mut self) {        
        if self.rotation == 0 {
            self.rotation = 3;
        } else{
            self.rotation -= 1;
        }
    }

    pub fn move_left(&mut self) {
        self.position.0 -= 1;
    }

    pub fn move_right(&mut self) {
        self.position.0 += 1;
    }

    pub fn move_down(&mut self) {
        self.position.1 += 1;
    }

    pub fn move_up(&mut self) {
        self.position.1 -= 1;
    }

    pub fn get_color(&self) -> usize {
        self.color
    }
}
